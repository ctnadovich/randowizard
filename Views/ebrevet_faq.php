<h3>At the finish of an event, what three things must be written on my paper brevet card?</h3>

<ol>
    <li>Your finish time.</li>
    <li>Your signature.</li>
    <li>The FINISH CODE from the app.</li>
</ol>

<p>If you used eBrevet and these three things are not on your brevet card at the finish, you can be disqualified. </p>

<h3>Do I get the same start time as everyone else?</h3>
<p>For a normal massed-start event, yes. It doesn't matter when you enter your start code and auto-check in to the first 
    control. So long as you enter the start code within an hour, before or after, the official time, your 
    event clock will start at the official start time like everyone else. So there's no need to wait till 
    the official time to enter your start code. Doing it early, say a half-hour before you start, this is OK.</p>

<h3>Can I use eBrevet for a Pre-Ride?</h3>
<p>Yes. eBrevet understands pre-rides. Within two weeks prior to the official event date (but not in the last 24 hours), a 'PreRide' button will appear 
    next to the event. Press the Pre-Ride button if you want to do a preride. You then need to check into the first control.
    Do this when you are about to start riding. Checking into the first control on a pre-ride starts your 
    clock for the ride. <i>Don't forget to check in to the first control!</i> If you forget, you're pretty much screwed as 
    your clock never started and none of the other controls will be available. </p>

<h2>Troubleshooting</h2>

<h3>Will eBrevet drain my battery?</h3>
   <p> No. eBrevet only needs to be activated at controls. It does not "track" you. 
    It does not run in the background. eBrevet works even if your phone is in "Airplane" mode. 
    You can turn your phone off or reboot it. </p>

    <h3>Does eBrevet continuously use the GPS draining the battery</h3>
    <p>No. Really. No. The GPS is only checked periodically, and only when you are looking at the control list page. 
        It's updated every 30 seconds by default, but if you want faster or slower updates, there is a configuration
        setting you can adjust.</p>
    </p>

    <h3>Does eBrevet need cell service or Internet access?</h3>
    <p>The only time you absolutely need Internet access is before the event to allow the phone to download event details. After you have the latest version of the route/cues downloaded to the phone, internet access is optional.</p> 
        
    <p>That said, there are some extra features when Internet is available. First of all, 
        it's good to occasionally turn on
    internet service and let the app "upload" your check-ins -- especially when you finish the event -- 
    so that your check ins can be tracked by your friends and your final time can be 
    communicated to the organizers.  In concert with that Internet upload of the check-ins, 
    if you have internet service you can see the checkins of <em>other</em> riders. This is most useful 
    to organizers, but sometimes its handy for riders to know when their comrades have checked in to other controls.
    </p>


<h3>The App says INVALID CODE when I enter my Start Code</h3>

<p>There are three reasons for a start code to be incorrect. If you are getting an incorrect start code
    error, check these three things:</p>
    <ol><li>You have old event data in your phone. Hit the button to update the event data to the latest.</li>
    <li>You mistyped the start code. Make sure you typed it correctly</li>
    <li>You do not have your correct Rider ID (RUSA Nmuber or equiv) entered in your phone. Check settings and make 
        sure your rider ID is correct.</li>
    </ol>

    </p> 

<h3>My brevet card is for version 6 of the route, but my phone says version 7. Am I screwed?</h3>

<p>eBrevet WILL allow someone to ride if the version on their phone is NEWER than the paperwork. If your brevet card has a start codes for, say, version 6 of the route, but for some reason your phone has a <em>newer</em> version, say version 7, that's fine. You can use the older brevet start code with the newer route.</p>

<p>But the reverse is not true. If your card is for version 7 and you still have version 6 in your phone, you will need to update the data on the phone. eBrevet will not allow you to start with a cue/route version OLDER than the brevet card. </p>

<h3>What if I "forget" to check in at a  control. </h3>

<p>If you "forget" to check in at a control the app will always notice. You will NOT get a finish code. The app will mark your 
    ride "RBA Review".  You must not skip any control. If you miss a control, you need to go back and check in (either with the app, or with traditional signature/receipt). If you skip controls you could be disqualified. </p>

<h3>When  I arrived at the overnight I checked in on eBrevet. 
    But later I looked an saw I could check into that same control again. 
    </h3>
<p>If the overnight is used multiple times, like in a cloverleaf or an out-n-back course, 
    then after you check in upon your arrival at the overnight -- by the current rules -- that same overnight control location is technically 
    open again for another checkin. Of course if you check in again, you will have skipped all the 
    intermediate controls between the first and second overnight. eBrevet will warn you that you are skipping 
    controls. Listen to the warning. Don't skip controls. </p>


<h3>What if I'm riding past the cutoff and I'm "late" to controls.</h3>
<p>Don't give up! Keep riding. Keep checking in. If the app complains, ignore it. Keep riding. Whether you are DQed is up to your RBA -- as long and you DO keep riding.</p>

<h3>I didn't get a green checkmark. Is that OK? What do these orange dots mean?</h3>
<p>When you check in at a control without live cellular Internet service the app will record your arrival internally and display orange dots indicating that the check-in has not been uploaded to the regional server. The next time you have Internet, if you check-in somewhere else or push the "Update Results" button your orange dots should turn into green checks indicating that your checkins have been communicated to the RBA. </p>

<h3>I just HATE these orange marks. They make me worry I'll be disqualified.</h3>
<p>The orange mark is PROOF that you checked in. The only reason it's orange rather than green is because 
    your phone has not been able to contact the main webserver at randonneuring.org and report your checkin there. That really 
    shouldn't concern you. Ride On!  Sometimes it helps to press the button to try again to report checkins -- that can 
make all the orange marks into green ones if communication can be established. But please don't worry if some of your checks are orange. You will still get a finish code 
if you properly check into all controls. You can write that finish code on your paper card. You'll still get a finish certificate. No worries. Seriously. Chill. </p>

<h3>I'm at the control but there's no Check-In button?</h3>
<p>If you are physically near (&LT;500 meters) a control at the correct time (after the control opens, before it closes) a "Check-In" 
    button will appear next to the control on the app. Sometimes it takes the GPS in your 
    phone  a few seconds to update your location. If you are impatient, press the "GPS UPDATE" button. 
    If the "Check-In" button still doesn't appear, maybe you aren't at the right place? Read the cue sheet or brevet card carefully 
    so you know where the control is. Another possibility is you've disallowed Location Permissions for the 
    eBrevet app. Check your phone Settings and make sure that eBrevet has LOCATION permission. The Activity Log (available through the main menu) often 
can give information about the app is thinking.</p>

<h3>But what if the app breaks or a dog eats my phone?</h3>
<p>Yes cell phones do sometimes fail, and software does sometimes have bugs. Prudent riders using eBrevet also write onto their paper brevet card their time of arrival and "control check in codes"  from the app at each intermediate control  as a paper backup.  Writing down your time and these codes isn't required -- it's simply prudent. If you think your phone is about to fail, battery die, or the app is acting wonky, record all check-in codes and be ready switch to old-school brevet card control check-in processes (get merchant initials, receipts, etc...) for the remainder of the event. The RBA will likely not accept 'my phone died' and a blank card as evidence you completed the event. </p>

<h3>What if I find a bug or want to suggest an improvement?</h3>
<p>You can <A HREF='https://github.com/ctnadovich/ebrevet/issues'>open 
    an issue or suggestion on github</a>.  Even better, you can fix the bug or implement the improvement yourself. eBrevet is 
open source software. Nothing prevents you from learning the system and making whatever changes you want. </p>

<h2>Conclusion</h2>

<p>Enjoy the blessings of technology in this app, but keep in mind that this is still randonneuring. Per our traditions, you as a rider must check-in at controls and must provide the required documentation for your ride.</p>



