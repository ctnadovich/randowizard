<h3>At the finish of an event, what three things must be written on my paper brevet card?</h3>

<ol>
    <li>Your finish time.</li>
    <li>Your signature.</li>
    <li>The FINISH CODE from the app.</li>
</ol>

<p>If you used eBrevet EPP and these three things are not on your brevet card at the finish, you can be disqualified. </p>

<h2>Troubleshooting</h2>

<h3>Will eBrevet drain my battery?</h3>
    No. eBrevet only needs to be activated at controls. It does not run in the background. eBrevet works even if your phone is in "Airplane" mode. You can turn your phone off or reboot it.

    <h3>Does eBrevet need cell service or Internet access?</h3>
    <p>The only time you absolutely need Internet access is before the event to allow the phone to download event details. After you have the latest version of the route/cues downloaded to the phone, internet access is optional. It's good to occasionally turn on
    internet service and let the app "upload" your check-ins -- especially when you finish the event -- so that your check ins can be tracked by your friends and your final time can be communicated to the organizers. Still, Internet service not a requirement except at the very beginning.
    </p>


<h3>The App says INVALID CODE when I enter my Start Code</h3>

<p>Make sure you have the latest version of the app and the latest version of the events downloaded into the app. The most common reason a start-code isn't accepted by the app is because either the events cue sheet has changed recently and you need to download the latest event info. Either that, or the app is out of date. </p> 

<h3>What if I "forget" to check in at a  control. </h3>

<p>If you "forget" to check in at a control the app will always notice. You will NOT get a finish code. The app will give you a DNQ.  You must not skip any control. If you miss a control, you need to go back and check in (either with the app, or with traditional signature/receipt). If you skip controls you could be disqualified. </p>

<h3>What if I'm riding past the cutoff and I'm "late" to controls.</h3>
<p>Don't give up! Keep riding. Keep checking in. If the app complains, ignore it. Keep riding. Whether you are DQed is up to your RBA -- as long and you DO keep riding.</p>

<h3>I didn't get a green checkmark. Is that OK? What do these orange dots mean?</h3>
<p>When you check in at a control without live cellular Internet service the app will record your arrival internally and display orange dots indicating that the check-in has not been uploaded to the regional server. The next time you have Internet, if you check-in somewhere else or push the "Update Results" button your orange dots should turn into green checks indicating that your checkins have been communicated to the RBA. </p>

<h3>I'm at the control but there's no Check-In button?</h3>
<p>If you are physically near (&LT;500 meters) a control at the correct time (after the control opens, before it closes) a "Check-In" button should appear next to the control on the app. Sometimes it takes the GPS in your phone  a few seconds to update your location. If you are impatient, press the "GPS UPDATE" button. If the "Check-In" button still doesn't appear, maybe you've disallowed Location Permissions for the eBrevet app. Check your phone Settings and make sure that eBrevet has LOCATION permission. </p>

<h3>But what if the app breaks or a dog eats my phone?</h3>
<p>Yes cell phones do sometimes fail, and software does sometimes have bugs. Prudent riders using EPP also write onto their paper brevet card their time of arrival and "control check in codes"  from the app at each intermediate control  as a paper backup.  Writing down your time and these codes isn't required -- it's simply prudent. If you think your phone is about to fail, battery die, or the app is acting wonky, record all check-in codes and be ready switch to old-school brevet card control check-in processes (get merchant initials, receipts, etc...) for the remainder of the event. The RBA will likely not accept 'my phone died' and a blank card as proof of passage. </p>

