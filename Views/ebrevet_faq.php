<h1>eBrevet FAQ</h1>

    <P>These are the most important things for riders to know before using eBrevet. If you'd like to know more, read the <A HREF='https://github.com/ctnadovich/ebrevet#readme'>Detailed Instructions for eBrevet</A> or <A HREF='https://nadovich.com/chris/contact.cgi'>Contact the author</a>. Organizers or RBAs should visit <A HREF=https://RANDONNEURING.ORG>RANDONNEURING.ORG</A> 
    </P>

    <h2>Introduction</h2>
    <p>The eBrevet cell phone app is now availible for brevets. Riders who have used this app generally report that it's fast, easy to operate, doesn't drain the phone battery, and is simpler than getting merchant signatures and receipts. You can use eBrevet exclusively on the ride without needing to get merchant signatures, receipts, answer info questions, or take photos.  Just ride the bike, and when you get to a control, click the button on eBrevet to check in. Then ride on.
</p>

    <h3>What is eBrevet?</h3>
    <p>eBrevet is an open-source Android/iOS app that serves as an automated, self-certifying brevet card for a randonneuring brevet or permanent. It maintains some of the "feel" of the traditional paper brevet card process. The app only needs to be activated at controls and does not require Internet data service at controls. Using the phone's clock and GPS, the app determines if you are near a control (within 500 meters). If a nearby control is open, you can record your authenticated check-in within the app. When network access happens to be available, the app will report these control check in times to the PA Rando server, which will be visible in real time on the roster page. When the event is completed successfully, the app can automatically upload results to Randonneuring.org, and generates a unique brevet Completion Certificate that is sharable on social media.</p>

    <h3>Where can I get eBrevet?</h3>
    <p>You can find the app on both the <A HREF='https://www.apple.com/app-store/'>Apple Store</A> and the <A HREF='https://play.google.com/'>Google Play Store</a>. Search for "eBrevet" by "CTNadovich". Please make sure you are using the latest version of the app. Control checkins or event downloads will not work if your app is outdated. </p>

    <h3>My region doesn't support eBrevet yet. What do they need to do?</h3>
    <p>Politely ask your RBA to take a look at <A HREF=https://RANDONNEURING.ORG>RANDONNEURING.ORG</A> and read the <A HREF=https://randonneuring.org/index.php/about/quick_start>Quick Start</A> guide. Basically all they need to do is enter their event details and grant riders permission to use the app. The rest is pretty much automatic.</p>

